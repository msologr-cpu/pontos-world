/* === COUNTER ANIMATION === */
function animateCounters() {
  document.querySelectorAll('.stat-num[data-target]').forEach(el => {
    const target = parseInt(el.dataset.target);
    const duration = 1800;
    const start = performance.now();
    function update(now) {
      const progress = Math.min((now - start) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      el.textContent = Math.floor(eased * target).toLocaleString('ru');
      if (progress < 1) requestAnimationFrame(update);
    }
    requestAnimationFrame(update);
  });
}

/* === SEA CANVAS === */
function initSeaCanvas() {
  const canvas = document.getElementById('seaCanvas');
  const ctx = canvas.getContext('2d');
  let w, h, waves = [];

  function resize() {
    w = canvas.width = window.innerWidth;
    h = canvas.height = window.innerHeight;
  }

  function initWaves() {
    waves = Array.from({length: 6}, (_, i) => ({
      y: h * 0.5 + i * 40,
      amplitude: 20 + i * 8,
      frequency: 0.004 + i * 0.001,
      speed: 0.3 + i * 0.1,
      phase: Math.random() * Math.PI * 2,
      opacity: 0.06 - i * 0.008
    }));
  }

  let t = 0;
  function draw() {
    ctx.clearRect(0, 0, w, h);
    waves.forEach(wave => {
      ctx.beginPath();
      ctx.moveTo(0, h);
      for (let x = 0; x <= w; x += 4) {
        const y = wave.y + Math.sin(x * wave.frequency + t * wave.speed + wave.phase) * wave.amplitude;
        ctx.lineTo(x, y);
      }
      ctx.lineTo(w, h); ctx.lineTo(0, h); ctx.closePath();
      ctx.fillStyle = `rgba(10, 60, 120, ${wave.opacity})`;
      ctx.fill();
    });
    t += 0.02;
    requestAnimationFrame(draw);
  }

  resize();
  initWaves();
  draw();
  window.addEventListener('resize', () => { resize(); initWaves(); });
}

